/*
    변환   함수
    1. 절댓값 : ABS()
    2. 제곱근 : SQRT()
    3. 부호판별 : SIGN()
    4. 제곱 : POWER(수, 제곱수)
    5. 나머지 : MOD(나눠질 수, 나눌 수)
    6. 올림 : CEIL()
    7. 내림 : FLOOR()
    8. 반올림 : ROUND(수, 소수자릿수)
    9. 절사 : TRUNC(수, 자릿수)
*/

SELECT ABS(-5) FROM DUAL;
SELECT SQRT(100) FROM DUAL;
SELECT SIGN(-5) FROM DUAL; -- (+1 / 0 / -1)
SELECT POWER(2, 10) FROM DUAL;
SELECT MOD(100, 3) FROM DUAL;
SELECT CEIL(1.000000000000000000000001) FROM DUAL;
SELECT CEIL(-1.000000000000000000000001) FROM DUAL;
SELECT FLOOR(1.99999999999999999999999) FROM DUAL;
SELECT FLOOR(-1.99999999999999999999999) FROM DUAL;
SELECT ROUND(123.456) FROM DUAL;
SELECT ROUND(123.456, 1) FROM DUAL;
SELECT ROUND(123.456, 100) FROM DUAL;
SELECT ROUND(123.456, -2) FROM DUAL;
SELECT ROUND(123.456, -3) FROM DUAL;
SELECT TRUNC(123.456) FROM DUAL;
SELECT TRUNC(123.456, 2) FROM DUAL;
SELECT TRUNC(123.456, 0) FROM DUAL;